// Translation Manager Class
class TranslationManager {
    constructor() {
        this.currentLang = localStorage.getItem('language') || 'en';
        this.init();
    }

    init() {
        // Set initial language
        this.applyTranslations();
        this.updateLanguageButton();
    }

    toggleLanguage() {
        this.currentLang = this.currentLang === 'en' ? 'hi' : 'en';
        localStorage.setItem('language', this.currentLang);
        this.applyTranslations();
        this.updateLanguageButton();
    }

    translate(key) {
        return translations[this.currentLang][key] || key;
    }

    applyTranslations() {
        // Update HTML lang attribute
        document.documentElement.lang = this.currentLang;

        // Translate all elements with data-translate attribute
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.translate(key);
            
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else if (element.tagName === 'SELECT' && element.options.length > 0) {
                // For select dropdowns, translate options individually
                const optionKey = element.getAttribute('data-translate-option');
                if (optionKey) {
                    Array.from(element.options).forEach((option, index) => {
                        if (option.value) {
                            option.text = this.translate(`${optionKey}.${option.value.toLowerCase().replace(/ /g, '.')}`);
                        } else {
                            option.text = this.translate(optionKey);
                        }
                    });
                }
            } else {
                element.textContent = translation;
            }
        });

        // Translate all elements with data-translate-html (for elements with HTML content)
        document.querySelectorAll('[data-translate-html]').forEach(element => {
            const key = element.getAttribute('data-translate-html');
            element.innerHTML = this.translate(key);
        });
    }

    updateLanguageButton() {
        const langBtn = document.getElementById('langBtn');
        if (langBtn) {
            langBtn.innerHTML = this.currentLang === 'en' 
                ? '<span class="lang-icon">🇮🇳</span> हिंदी' 
                : '<span class="lang-icon">🇬🇧</span> English';
        }
    }

    getCurrentLanguage() {
        return this.currentLang;
    }
}

// Initialize translation manager
const translationManager = new TranslationManager();

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { TranslationManager, translationManager };
}
